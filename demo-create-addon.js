#!/usr/bin/env node

// demo-create-addon.js - Demonstration of creating a WPS add-in

console.log('WPS.js Add-in Creation Demo');
console.log('==========================\n');

console.log('This script demonstrates how to create a new WPS add-in using wpsjs.\n');

console.log('Step 1: Create a new WPS add-in project');
console.log('----------------------------------------');
console.log('Run the following command in your terminal:');
console.log('npx wpsjs create my-sample-addon\n');

console.log('Step 2: Navigate to the project directory');
console.log('-----------------------------------------');
console.log('cd my-sample-addon\n');

console.log('Step 3: Install project dependencies');
console.log('------------------------------------');
console.log('npm install\n');

console.log('Step 4: Start the development server');
console.log('-----------------------------------');
console.log('npm start\n');

console.log('Step 5: Debug your add-in');
console.log('-------------------------');
console.log('npx wpsjs debug\n');

console.log('Additional Commands:');
console.log('--------------------');
console.log('- Build for production: npx wpsjs build');
console.log('- Publish your add-in: npx wpsjs publish');
console.log('- Unpublish your add-in: npx wpsjs unpublish\n');

console.log('For more information:');
console.log('--------------------');
console.log('- Visit: https://www.npmjs.com/package/wpsjs');
console.log('- Join QQ group: 185170323 for support and discussion\n');

console.log('Note: This is a demonstration script. To actually create a WPS add-in,');
console.log('run the commands shown above in your terminal.');
