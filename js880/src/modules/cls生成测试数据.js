/**
 * 从"数据"表获取测试数据
 * @returns {Array2D} 测试数据
 */
function 获取测试数据() {
    var dataSheet = Application.ActiveWorkbook.Worksheets("数据");
    if (!dataSheet) {
        throw new Error("找不到'数据'表，请先打开测试文件");
    }

    var usedRange = dataSheet.UsedRange;

    // 查看数据结构
    var rowCount = usedRange.Rows.Count;
    var colCount = usedRange.Columns.Count;

    // 限制数据量 - 最多取100行（含表头）
    var maxRows = 100;
    var dataRows = Math.min(rowCount, maxRows);

    console.log("   读取范围: " + dataRows + " 行 × " + colCount + " 列");

    // 只读取需要的数据范围
    var dataRange = dataSheet.Range(usedRange.Cells(1, 1), usedRange.Cells(dataRows, colCount));

    // 转换为 Array2D (添加 RngUtils 安全检查)
    var arr;
    if (typeof RngUtils !== 'undefined' && typeof RngUtils.safeArray === 'function') {
        arr = RngUtils.safeArray(dataRange);
    } else {
        // Fallback: 直接读取 range 值并包装为 Array2D
        var rawData = dataRange.Value2;
        arr = new Array2D(rawData);
    }

    // 防御性检查：确保返回的数据行数不超过预期
    var actualCount = arr.length;
    console.log("   RngUtils.safeArray() 返回: " + actualCount + " 行");

    if (actualCount > dataRows) {
        console.log("   检测到数据超出预期，截取前 " + dataRows + " 行");
        var temp = [];
        for (var i = 0; i < dataRows; i++) {
            temp.push(arr[i]);
        }
        arr = new Array2D(temp);
        console.log("   截取后: " + arr.length + " 行");
    }

    console.log("   实际返回: " + arr.length + " 行");

    return arr;
}

/**
 * 清空测试区域
 * @returns {Worksheet} 测试输出工作表
 */
function 清空测试区域() {
    var wb = Application.ActiveWorkbook;
    var ws = null;

    // 尝试获取"测试输出"表
    try {
        ws = wb.Worksheets("测试输出");
        ws.Cells.Clear();
        console.log("已清空'测试输出'表");
    } catch (e) {
        // 工作表不存在，创建新的
        ws = wb.Worksheets.Add();
        ws.Name = "测试输出";
        console.log("已创建'测试输出'表");
    }

    return ws;
}

